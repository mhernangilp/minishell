#include "../libft.h"

int	ft_lstsize(t_list *lst)
{
	int		i;
	t_list	*p;

	i = 1;
	p = lst;
	if (!p)
		return (0);
	while (p -> next)
	{
		p = p -> next;
		i++;
	}
	return (i);
}
